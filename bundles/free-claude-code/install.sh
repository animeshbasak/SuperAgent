#!/usr/bin/env bash
# ─────────────────────────────────────────────────────────────────────────────
# SuperAgent Bundle — free-claude-code
# Vendors Alishahryar1/free-claude-code via git-clone + uv venv (Python 3.14+).
# Not on PyPI, so we skip pipx and manage the project locally.
# Footprint: ~400 MB (uv + venv + repo).
# Idempotent: re-running git-pulls and re-syncs the venv.
# Usage: bash bundles/free-claude-code/install.sh
# ─────────────────────────────────────────────────────────────────────────────
set -euo pipefail

# ── Colors ────────────────────────────────────────────────────────────────────
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'; CYAN='\033[0;36m'
BOLD='\033[1m'; NC='\033[0m'
ok()   { echo -e "${GREEN}✓${NC} $*"; }
info() { echo -e "${CYAN}→${NC} $*"; }
warn() { echo -e "${YELLOW}!${NC} $*"; }
fail() { echo -e "${RED}✗ $*${NC}" >&2; exit 1; }

# ── OS detection ──────────────────────────────────────────────────────────────
OS="$(uname -s)"
case "$OS" in
  Darwin|Linux) ;;
  *) fail "Unsupported OS: $OS (need Darwin or Linux)" ;;
esac

REPO_URL="https://github.com/Alishahryar1/free-claude-code"
INSTALL_DIR="$HOME/.superagent/free-claude-code"
LAUNCHER="$HOME/.local/bin/free-claude-code-server"
PORT=18082

echo ""
echo -e "${BOLD}── free-claude-code bundle ──${NC}"

# ── Step 1: Ensure git ────────────────────────────────────────────────────────
command -v git >/dev/null 2>&1 || fail "git is required"

# ── Step 2: Ensure uv (handles Python 3.14+ for us) ───────────────────────────
ensure_uv() {
  if command -v uv >/dev/null 2>&1; then
    ok "uv already installed ($(uv --version 2>/dev/null | head -1))"
    return
  fi
  info "Installing uv (astral.sh)..."
  curl -LsSf https://astral.sh/uv/install.sh | sh
  # Make uv visible in this shell
  export PATH="$HOME/.local/bin:$HOME/.cargo/bin:$PATH"
  command -v uv >/dev/null 2>&1 || fail "uv install failed (not on PATH after install)"
  ok "uv installed"
}
ensure_uv

# ── Step 3: Clone or update repo ──────────────────────────────────────────────
mkdir -p "$(dirname "$INSTALL_DIR")"
if [[ -d "$INSTALL_DIR/.git" ]]; then
  info "Repo exists — git pull..."
  git -C "$INSTALL_DIR" fetch --quiet origin
  git -C "$INSTALL_DIR" pull --quiet --ff-only || warn "git pull failed (local changes?) — continuing"
  ok "Repo updated"
else
  if [[ -d "$INSTALL_DIR" ]]; then
    fail "$INSTALL_DIR exists but is not a git checkout — remove it and re-run"
  fi
  info "Cloning $REPO_URL → $INSTALL_DIR"
  git clone --depth 1 "$REPO_URL" "$INSTALL_DIR"
  ok "Repo cloned"
fi

# ── Step 4: uv venv + sync ────────────────────────────────────────────────────
info "Creating venv with Python 3.14..."
(
  cd "$INSTALL_DIR"
  # uv will fetch Python 3.14 if not present locally
  if [[ ! -d ".venv" ]]; then
    uv venv --python 3.14 || uv venv --python 3.14 --allow-existing
  fi
  if [[ -f "uv.lock" ]] || [[ -f "pyproject.toml" ]]; then
    info "uv sync..."
    uv sync || warn "uv sync reported issues — check $INSTALL_DIR"
  else
    warn "No pyproject.toml/uv.lock found — repo layout may have changed"
  fi
)
ok "venv ready at $INSTALL_DIR/.venv"

# ── Step 5: Launcher script ───────────────────────────────────────────────────
mkdir -p "$(dirname "$LAUNCHER")"
cat > "$LAUNCHER" <<EOF
#!/usr/bin/env bash
# free-claude-code launcher (generated by SuperAgent bundle installer)
# Runs the server on 127.0.0.1:${PORT}
set -euo pipefail
cd "${INSTALL_DIR}"
exec uv run uvicorn server:app --host 127.0.0.1 --port ${PORT} "\$@"
EOF
chmod +x "$LAUNCHER"
ok "Launcher: $LAUNCHER (port :${PORT})"

# ── Self-verify ───────────────────────────────────────────────────────────────
echo ""
info "Self-verification..."
[[ -x "$LAUNCHER" ]] || fail "verify: launcher missing or not executable"
[[ -d "$INSTALL_DIR/.venv" ]] || fail "verify: .venv not created"
command -v uv >/dev/null 2>&1 || fail "verify: uv not on PATH"
ok "launcher executable: $LAUNCHER"
ok "venv present: $INSTALL_DIR/.venv"

echo ""
ok "free-claude-code bundle ready"
echo "  Start with: free-claude-code-server"
echo "  Listens on: http://127.0.0.1:${PORT}"
echo ""
